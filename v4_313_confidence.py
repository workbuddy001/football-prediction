# 档位量化
rank_values = {"人强": 7, "普强": 6, "准强": 5, "中上": 4, "中游": 3, "中下": 2, "下游": 1}

def get_distribution_type(match):
    主胜, 平局, 客胜 = match["主胜"], match["平局"], match["客胜"]
    状态差距 = match["主队状态"] - match["客队状态"]
    档位差距 = rank_values.get(match["主队档位"], 3) - rank_values.get(match["客队档位"], 3)
    max_odd, min_odd = max(主胜, 平局, 客胜), min(主胜, 平局, 客胜)
    赔率比 = max_odd / min_odd
    
    if 档位差距 >= 2 and abs(状态差距) > 15 and 赔率比 > 2:
        return "顺分布"
    if abs(档位差距) < 2 and abs(状态差距) < 15 and 1.3 < 赔率比 < 2:
        return "逆分布"
    if abs(档位差距) < 2 and abs(状态差距) < 15 and 赔率比 < 1.5:
        return "中庸分布"
    return "缓冲分布"

def analyze_match_v4(match):
    """V4优化版分析 + 信心度评级"""
    主胜, 平局, 客胜 = match["主胜"], match["平局"], match["客胜"]
    推荐, 状态差距 = match["推荐"], match["主队状态"] - match["客队状态"]
    分布类型 = get_distribution_type(match)
    档位差距 = rank_values.get(match["主队档位"], 3) - rank_values.get(match["客队档位"], 3)
    
    # === 信心度评级 ===
    
    # A级（90%+）：极低赔 + 档位差距大 + 状态差距大
    if 主胜 < 1.35 and 档位差距 >= 2 and 状态差距 > 20:
        return "主胜", "A级", "极低赔+档位差距大+状态差距大"
    
    if 客胜 < 1.35 and 档位差距 <= -2 and 状态差距 < -20:
        return "客胜", "A级", "极低赔+档位差距大+状态差距大"
    
    # A级：低赔(<1.5) + 状态极佳(>65)
    if 主胜 < 1.5 and match["主队状态"] > 65:
        return "主胜", "A级", "低赔+状态极佳"
    
    if 客胜 < 1.5 and match["客队状态"] > 65:
        return "客胜", "A级", "低赔+状态极佳"
    
    # B级（70-90%）：顺分布+低赔
    if 分布类型 == "顺分布":
        if 主胜 < 1.8:
            return "主胜", "B级", "顺分布+低赔"
        if 客胜 < 1.8:
            return "客胜", "B级", "顺分布+低赔"
    
    # B级：逆分布+澳门推荐平局+低平赔
    if 分布类型 in ["逆分布", "中庸分布"]:
        if 推荐 == "平" and 平局 < 3.5:
            return "防平", "B级", f"{分布类型}+澳门推荐平局+低平赔"
        if abs(状态差距) < 15 and 平局 < 3.2:
            return "防平", "B级", f"{分布类型}+状态接近+低平赔"
    
    # C级（50-70%）：缓冲分布+澳门推荐
    if 分布类型 == "缓冲分布":
        if 推荐 == "胜":
            return "主胜", "C级", "缓冲分布+澳门推荐胜"
        elif 推荐 == "负":
            return "客胜", "C级", "缓冲分布+澳门推荐负"
        else:
            return "防平", "C级", "缓冲分布+澳门推荐平"
    
    # D级（<50%）：其他
    if 推荐 == "胜":
        return "主胜", "D级", "其他"
    elif 推荐 == "负":
        return "客胜", "D级", "其他"
    return "防平", "D级", "其他"

