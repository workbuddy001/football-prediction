# 档位量化
rank_values = {
    "人强": 7, "普强": 6, "准强": 5, "中上": 4, "中游": 3, "中下": 2, "下游": 1
}

def get_distribution_type(match):
    """
    判断分布类型
    基于档位差距 + 状态差距 + 赔率差距综合判断
    """
    主胜 = match["主胜"]
    平局 = match["平局"]
    客胜 = match["客胜"]
    主队状态 = match["主队状态"]
    客队状态 = match["客队状态"]
    主队档 = match.get("主队档位", "中游")
    客队档 = match.get("客队档位", "中游")
    
    # 档位差距
    档位差距 = rank_values.get(主队档, 3) - rank_values.get(客队档, 3)
    
    # 状态差距
    状态差距 = 主队状态 - 客队状态
    
    # 赔率差距
    max_odd = max(主胜, 平局, 客胜)
    min_odd = min(主胜, 平局, 客胜)
    赔率比 = max_odd / min_odd
    
    # 判断分布类型
    # 顺分布：档位差距大（>=2档），状态差距大（>15），赔率差距大（>2）
    if 档位差距 >= 2 and abs(状态差距) > 15 and 赔率比 > 2:
        return "顺分布"
    
    # 逆分布：档位接近（差距<2），状态接近（差距<15），但赔率有差距
    if abs(档位差距) < 2 and abs(状态差距) < 15 and 1.3 < 赔率比 < 2:
        return "逆分布"
    
    # 中庸分布：档位接近，状态接近，赔率接近
    if abs(档位差距) < 2 and abs(状态差距) < 15 and 赔率比 < 1.5:
        return "中庸分布"
    
    # 缓冲分布：其他情况
    return "缓冲分布"

def analyze_match_v4(match):
    """V4优化版分析 - 基于分布思维"""
    主胜 = match["主胜"]
    平局 = match["平局"]
    客胜 = match["客胜"]
    推荐 = match["推荐"]
    主队状态 = match["主队状态"]
    客队状态 = match["客队状态"]
    主队档 = match.get("主队档位", "中游")
    客队档 = match.get("客队档位", "中游")
    
    状态差距 = 主队状态 - 客队状态
    分布类型 = get_distribution_type(match)
    
    # === V4优化版核心规则 ===
    
    # A级：极低赔 + 档位差距大（顺分布最强信号）
    if 主胜 < 1.35 and 状态差距 > 20:
        return "主胜", "A级", "极低赔+档位差距大=顺分布实盘"
    
    if 客胜 < 1.35 and 状态差距 < -20:
        return "客胜", "A级", "极低赔+档位差距大=顺分布实盘"
    
    # A级：低赔 + 状态极佳
    if 主胜 < 1.5 and 主队状态 > 65:
        return "主胜", "A级", "低赔+状态佳=顺分布"
    
    if 客胜 < 1.5 and 客队状态 > 65:
        return "客胜", "A级", "低赔+状态佳=顺分布"
    
    # B级：逆分布时重点关注平局
    if 分布类型 == "逆分布" or 分布类型 == "中庸分布":
        # 逆分布时澳门推荐平局 = 防平
        if 推荐 == "平":
            return "防平", "B级", f"{分布类型}+澳门推荐平局"
        
        # 逆分布时状态接近 + 赔率接近 = 防平
        if abs(状态差距) < 15:
            if 平局 < 3.5:
                return "防平", "B级", f"{分布类型}+状态接近+低平"
    
    # B级：顺分布时信任低赔
    if 分布类型 == "顺分布":
        if 主胜 < 1.7:
            return "主胜", "B级", "顺分布+低赔"
        if 客胜 < 1.7:
            return "客胜", "B级", "顺分布+低赔"
    
    # C级：跟随澳门推荐
    if 推荐 == "胜":
        return "主胜", "C级", "默认跟随澳门"
    elif 推荐 == "负":
        return "客胜", "C级", "默认跟随澳门"
    else:
        return "防平", "C级", "默认防平"

