# 简化版双选策略
def get_double_choice_v2(prediction, match):
    """优化版双选策略"""
    主胜 = match["主胜"]
    平局 = match["平局"]
    客胜 = match["客胜"]
    主队状态 = match["主队状态"]
    客队状态 = match["客队状态"]
    推荐 = match["推荐"]
    
    状态差距 = abs(主队状态 - 客队状态)
    max_odd = max(主胜, 平局, 客胜)
    min_odd = min(主胜, 平局, 客胜)
    
    需要双选 = False
    双选 = ""
    
    # 策略1：状态接近（差距<10）+ 赔率接近（比值<1.4）
    if 状态差距 < 10 and max_odd / min_odd < 1.4:
        需要双选 = True
        if prediction == "主胜":
            双选 = "主胜/平局"
        elif prediction == "客胜":
            双选 = "客胜/平局"
        else:
            双选 = prediction
    
    # 策略2：极低赔(<1.35) + 平局赔率<4.0 时防平
    if (主胜 < 1.35 or 客胜 < 1.35) and 平局 < 4.0:
        需要双选 = True
        if 主胜 < 1.35:
            双选 = "主胜/平局"
        else:
            双选 = "客胜/平局"
    
    # 策略3：澳门推荐与预测相反时
    if (推荐 == "胜" and prediction == "防平") or (推荐 == "负" and prediction == "防平"):
        需要双选 = True
        if 推荐 == "胜":
            双选 = "防平/主胜"
        else:
            双选 = "防平/客胜"
    
    return 需要双选, 双选

