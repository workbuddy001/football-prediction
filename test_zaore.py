def calc_zaore(h, d, a, macao, form_diff, home_team="", away_team=""):
    """造热三维度判断"""
    total_change = abs(h) + abs(d) + abs(a)
    
    # 维度1：幅度
    if total_change <= 10:
        return "无造热", "变化太小", 0, "无"
    
    # 判断格局和出口方向
    down_dirs = []
    up_dirs = []
    for name, val in [("主", h), ("平", d), ("客", a)]:
        if val < -2:  # 降>2%才算有意义
            down_dirs.append(name)
        elif val > 2:  # 升>2%才算有意义
            up_dirs.append(name)
    
    if len(down_dirs) >= 2:
        return "无造热", f"双出口分流{down_dirs}", 0, "无"
    
    if len(down_dirs) == 0:
        return "无造热", "无明确下降方向", 0, "无"
    
    # 单出口
    heat_dir = down_dirs[0]  # 造热方向
    
    # 判断格局
    if len(up_dirs) >= 2:
        pattern = f"一降({heat_dir})两升"
    elif len(up_dirs) == 1:
        pattern = f"一降({heat_dir})一升({up_dirs[0]})"
    else:
        pattern = "格局不明"
    
    # 维度3：澳门同向
    macao_dir = ""
    if "主" in macao:
        macao_dir = "主"
    elif "客" in macao:
        macao_dir = "客"
    elif "和" in macao or "平" in macao:
        macao_dir = "平"
    
    if macao_dir != heat_dir:
        return "无造热", f"{pattern}，澳门推{macao_dir}≠{heat_dir}", total_change, heat_dir
    
    # 基本面支持（近况差）
    if heat_dir == "主" and form_diff < 2:
        return "无造热", f"{pattern}，近况差{form_diff:+d}不支持主", total_change, heat_dir
    if heat_dir == "客" and form_diff > -2:
        return "无造热", f"{pattern}，近况差{form_diff:+d}不支持客", total_change, heat_dir
    
    # 维度4：基本面分流（强队/弱队判断）
    # 弱队/无名队名单（需要手动标注，这里用简化的判断）
    weak_teams = ["博德", "叻武里", "淡宾尼士", "新未来SC", "神户胜利"]
    strong_teams = ["阿森纳", "曼城", "巴黎圣曼", "切尔西", "南安普敦", "多特蒙德", "尤文图斯",
                    "全北现代", "本菲卡", "AC米兰", "皇马", "利物浦", "拜仁"]
    
    if heat_dir == "主":
        team_name = home_team
    else:
        team_name = away_team
    
    is_weak = any(w in team_name for w in weak_teams)
    is_strong = any(s in team_name for s in strong_teams)
    
    if is_weak:
        return "假造热", f"{pattern}，澳门同向，但{team_name}弱队→筹码能分流", total_change, heat_dir
    else:
        return "真造热", f"{pattern}，澳门同向，近况支持", total_change, heat_dir

