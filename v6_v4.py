# V6算法 v4 - 每场比赛都判断实盘/诱盘

def get_distribution(home, draw, away):
    """判断分布类型"""
    if home < away and home < 2.0:
        if away - home > 0.5:
            return "顺分布"
        elif away - home > 0.2:
            return "缓冲分布"
        else:
            return "中庸分布"
    elif away < home and away < 2.0:
        if home - away > 0.5:
            return "逆分布"
        elif home - away > 0.2:
            return "缓冲分布"
        else:
            return "中庸分布"
    else:
        return "中庸分布"


def analyze_odds_change_30(initial_odds, realtime_odds):
    """统计30家公司赔率变化"""
    home_up_10 = home_down_10 = 0
    draw_up_10 = draw_down_10 = 0
    away_up_10 = away_down_10 = 0
    
    # 统计所有变化方向
    home_up = home_down = 0
    draw_up = draw_down = 0
    away_up = away_down = 0
    
    total = min(len(initial_odds), len(realtime_odds))
    
    for i in range(total):
        ih, id, ia = initial_odds[i]
        rh, rd, ra = realtime_odds[i]
        
        # 主胜
        if ih > 0:
            pct = (rh - ih) / ih * 100
            if pct >= 10:
                home_up_10 += 1
                home_up += 1
            elif pct <= -10:
                home_down_10 += 1
                home_down += 1
            elif pct > 0:
                home_up += 1
            else:
                home_down += 1
        
        # 平局
        if id > 0:
            pct = (rd - id) / id * 100
            if pct >= 10:
                draw_up_10 += 1
                draw_up += 1
            elif pct <= -10:
                draw_down_10 += 1
                draw_down += 1
            elif pct > 0:
                draw_up += 1
            else:
                draw_down += 1
        
        # 客胜
        if ia > 0:
            pct = (ra - ia) / ia * 100
            if pct >= 10:
                away_up_10 += 1
                away_up += 1
            elif pct <= -10:
                away_down_10 += 1
                away_down += 1
            elif pct > 0:
                away_up += 1
            else:
                away_down += 1
    
    return {
        "home_up_10": home_up_10, "home_down_10": home_down_10,
        "draw_up_10": draw_up_10, "draw_down_10": draw_down_10,
        "away_up_10": away_up_10, "away_down_10": away_down_10,
        "home_up": home_up, "home_down": home_down,
        "draw_up": draw_up, "draw_down": draw_down,
        "away_up": away_up, "away_down": away_down,
        "total": total
    }


def analyze_draw_trend(stats):
    """
    分析平局趋势
    返回: "up_high"=升赔多且幅度大(排除), "down"=降赔多(防平), "neutral"=其他
    
    核心逻辑：
    - 升赔公司占比 >= 70% → 排除平局（庄家不担心平局打出）
    - 升10%以上公司占比 >= 50% → 排除平局
    - 降赔公司占比 >= 50% → 防平（庄家担心平局打出）
    - 其他 → 可博平局
    """
    draw_up = stats["draw_up"]
    draw_down = stats["draw_down"]
    draw_up_10 = stats["draw_up_10"]  # 升幅>=10%的公司
    total = stats["total"]
    
    up_ratio = draw_up / total if total > 0 else 0
    down_ratio = draw_down / total if total > 0 else 0
    up_10_ratio = draw_up_10 / total if total > 0 else 0
    
    # 升赔公司占比 >= 70%，且升10%以上公司占比 >= 50% → 排除平局
    if up_ratio >= 0.7 and up_10_ratio >= 0.5:
        return "up_high", "排除平局"
    
    # 升赔公司占比 >= 70% → 排除平局
    if up_ratio >= 0.7:
        return "up_high", "排除平局"
    
    # 升10%以上公司占比 >= 60% → 排除平局
    if up_10_ratio >= 0.6:
        return "up_high", "排除平局"
    
    # 降赔公司占比 >= 50% → 防平（庄家担心平局打出）
    if down_ratio >= 0.5:
        return "down", "防平"
    
    # 升赔和降赔都存在且比例接近 → 排除平局（态度不明）
    if draw_up > 0 and draw_down > 0 and abs(up_ratio - down_ratio) < 0.2:
        return "neutral", "排除平局"
    
    return "neutral", "可博平局"


def judge_real_or_trap(macao_tip, home_team, away_team, home, draw, away, stats):
    """
    判断实盘/诱盘
    核心逻辑：
    1. 平赔分析：升多→排除平局，降多→防平
    2. 澳门推荐主/客胜：升→诱盘(反选)，降→实盘(跟)
    3. 澳门推荐和局：平赔降多→信和局，升多→排除
    """
    total = stats["total"]
    draw_trend, draw_tip = analyze_draw_trend(stats)
    
    # 解析澳门推荐
    macao_home = home_team in macao_tip and ("赢" in macao_tip or "贏" in macao_tip)
    macao_away = away_team in macao_tip and ("赢" in macao_tip or "贏" in macao_tip)
    macao_draw = "和局" in macao_tip
    
    # ===== 澳门推荐主胜 =====
    if macao_home:
        # 平赔上升趋势 → 排除平局，反向
        if draw_trend == "up_high":
            if away < home + 0.3:
                return "诱盘", "客胜", f"平升{stats['draw_up']}家排除,客胜{away}→诱盘"
            else:
                return "诱盘", "主胜", f"平升{stats['draw_up']}家排除,主胜低{home}"
        # 平赔下降趋势 → 防平
        elif draw_trend == "down":
            # 诱盘逻辑不变
            if stats["home_up_10"] >= stats["total"] * 0.3:
                return "诱盘", "防平", f"平降多防平,主胜升10%↑{stats['home_up_10']}家→诱盘"
            elif stats["home_down_10"] >= stats["total"] * 0.3:
                return "实盘", "主胜", f"平降多防平,主胜降10%↓{stats['home_down_10']}家→实盘"
            else:
                return "待定", "防平", f"平降多防平"
        
        # 其他情况，按原有逻辑
        if stats["home_up_10"] >= stats["total"] * 0.3:
            return "诱盘", "防平", f"主胜升10%↑{stats['home_up_10']}家"
        elif stats["home_down_10"] >= stats["total"] * 0.3:
            return "实盘", "主胜", f"主胜降10%↓{stats['home_down_10']}家"
        elif stats["home_up"] > stats["home_down"]:
            return "诱盘", "防平", f"主胜升{stats['home_up']}/{total}家"
        else:
            return "实盘", "主胜", f"主胜降{stats['home_down']}/{total}家"
    
    # ===== 澳门推荐客胜 =====
    elif macao_away:
        # 平赔上升趋势 → 排除平局，反向
        if draw_trend == "up_high":
            if home < away + 0.3:
                return "诱盘", "主胜", f"平升{stats['draw_up']}家排除,主胜{home}→诱盘"
            else:
                return "诱盘", "客胜", f"平升{stats['draw_up']}家排除,客胜低{away}"
        # 平赔下降趋势 → 防平
        elif draw_trend == "down":
            if stats["away_up_10"] >= stats["total"] * 0.3:
                return "诱盘", "防平", f"平降多防平,客胜升10%↑{stats['away_up_10']}家→诱盘"
            elif stats["away_down_10"] >= stats["total"] * 0.3:
                return "实盘", "客胜", f"平降多防平,客胜降10%↓{stats['away_down_10']}家→实盘"
            else:
                return "待定", "防平", f"平降多防平"
        
        # 其他情况，按原有逻辑
        elif stats["away_up_10"] >= stats["total"] * 0.3:
            return "诱盘", "防平", f"客胜升10%↑{stats['away_up_10']}家"
        elif stats["away_down_10"] >= stats["total"] * 0.3:
            return "实盘", "客胜", f"客胜降10%↓{stats['away_down_10']}家"
        elif stats["away_up"] > stats["away_down"]:
            return "诱盘", "防平", f"客胜升{stats['away_up']}/{total}家"
        else:
            return "实盘", "客胜", f"客胜降{stats['away_down']}/{total}家"
    
    # ===== 澳门推荐和局 =====
    elif macao_draw:
        # 澳门推荐和局时：即使升也要防平，不完全排除
        if draw_trend == "up_high":
            return "待定", "防平", f"澳门和局,平升{stats['draw_up']}家→防平"
        # 平赔下降趋势 → 防平（庄家担心平局）
        elif draw_trend == "down":
            return "待定", "防平", f"平降{stats['draw_down']}家→防平"
        # 中性 → 可博和局
        else:
            return "实盘", "和局", f"澳门推荐和局,平赔统一→信"
    
    # ===== 无澳门推荐时，用分布+平赔趋势 =====
    else:
        # 平赔上升 → 排除平局
        if draw_trend == "up_high":
            dist = get_distribution(home, draw, away)
            if dist == "顺分布":
                return "待定", "主胜", f"平升排除,顺分布主胜"
            elif dist == "逆分布":
                return "待定", "客胜", f"平升排除,逆分布客胜"
            else:
                return "待定", "主胜" if home < away else "待定", "客胜"
        
        # 平赔下降 → 防平
        elif draw_trend == "down":
            dist = get_distribution(home, draw, away)
            if dist == "顺分布":
                return "待定", "防平", f"平降防平,顺分布防平"
            elif dist == "逆分布":
                return "待定", "防平", f"平降防平,逆分布防平"
            else:
                return "待定", "防平", f"平降防平"
        
        # 中性
        dist = get_distribution(home, draw, away)
        if dist == "顺分布":
            if home < 1.8:
                return "实盘", "主胜", f"顺分布-主胜低赔{home}"
            else:
                return "待定", "防平", f"顺分布-高赔防平"
        elif dist == "逆分布":
            if away < 1.8:
                return "实盘", "客胜", f"逆分布-客胜低赔{away}"
            else:
                return "待定", "防平", f"逆分布-防平"
        else:
            if draw < 3.3 and stats["draw_up"] > stats["draw_down"]:
                return "实盘", "和局", f"中庸-低平{draw}+升"
            return "待定", "防平", f"中庸-防平"
            dist = get_distribution(home, draw, away)
            if dist == "顺分布":
                return "待定", "主胜", f"平升排除,顺分布主胜"
            elif dist == "逆分布":
                return "待定", "客胜", f"平升排除,逆分布客胜"
            else:
                # 中庸分布：选赔率更低的一方
                if home < away:
                    return "待定", "主胜", f"平赔不统一→排除,中庸主胜"
                else:
                    return "待定", "客胜", f"平赔不统一→排除,中庸客胜"
        
        dist = get_distribution(home, draw, away)
        
        # 顺分布：主胜热门
        if dist == "顺分布":
            if home < 1.8:
                return "实盘", "主胜", f"顺分布-主胜低赔{home}"
            else:
                return "待定", "防平", f"顺分布-高赔防平"
        
        # 逆分布：客胜热门
        elif dist == "逆分布":
            if away < 1.8:
                return "实盘", "客胜", f"逆分布-客胜低赔{away}"
            else:
                return "待定", "防平", f"逆分布-防平"
        
        # 中庸分布
        else:
            # 平局偏低且统一上升
            if draw < 3.3 and stats["draw_up"] > stats["draw_down"]:
                return "实盘", "和局", f"中庸-低平{draw}+统一升"
            return "待定", "防平", f"中庸-防平"


