    """V6预测 v4"""
    distribution = get_distribution(home, draw, away)
    stats = analyze_odds_change_30(initial_odds, realtime_odds)
    
    # 调试：打印平赔趋势（仅部分比赛）
    # draw_trend, draw_tip = analyze_draw_trend(stats)
    # if "和局" in macao_tip:
    #     print(f"  → 平:升{stats['draw_up']}/降{stats['draw_down']}/升10%{stats['draw_up_10']}家")
    
    trap_type, result, reason = judge_real_or_trap(
        macao_tip, home_team, away_team, home, draw, away, stats
    )
    
    confidence = "A级" if "实盘" in trap_type or "诱盘" in trap_type else "B级"
    
    return result, confidence, distribution, f"{trap_type}-{reason}", stats


