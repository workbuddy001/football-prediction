def get_double_choice_tip(prediction, match):
    """
    根据不确定情况给出双选建议
    策略：状态相近 + 赔率接近 时增加双选
    """
    主胜 = match["主胜"]
    平局 = match["平局"]
    客胜 = match["客胜"]
    主队状态 = match["主队状态"]
    客队状态 = match["客队状态"]
    推荐 = match["推荐"]
    
    状态差距 = abs(主队状态 - 客队状态)
    max_odd = max(主胜, 平局, 客胜)
    min_odd = min(主胜, 平局, 客胜)
    odds_ratio = max_odd / min_odd  # 赔率差距
    
    # 判断是否需要双选
    # 条件1：状态接近（差距<15）
    # 条件2：赔率接近（比值<1.5）
    # 条件3：预测与澳门推荐不一致
    
    need_double = False
    double_choice = ""
    
    if 状态差距 < 15 and odds_ratio < 1.5:
        # 状态接近 + 赔率接近 = 难以判断，增加双选
        need_double = True
        if prediction == "主胜":
            # 主胜优先，但防平
            if 平局 < 主胜 * 1.3:
                double_choice = "主胜/平局"
        elif prediction == "客胜":
            # 客胜优先，但防平
            if 平局 < 客胜 * 1.3:
                double_choice = "客胜/平局"
        elif prediction == "防平":
            # 已经防平了，检查是否需要防主胜/客胜
            if 主胜 < 客胜 * 0.8:
                double_choice = "防平/主胜"
            elif 客胜 < 主胜 * 0.8:
                double_choice = "防平/客胜"
    
    # 特殊判断：澳门推荐与预测不一致
    if 推荐 == "胜" and prediction == "防平":
        need_double = True
        double_choice = "防平/主胜"
    elif 推荐 == "负" and prediction == "防平":
        need_double = True
        double_choice = "防平/客胜"
    
    if need_double and not double_choice:
        # 默认双选
        if prediction == "主胜":
            double_choice = "主胜/平局"
        elif prediction == "客胜":
            double_choice = "客胜/平局"
        else:
            double_choice = prediction  # 已经是防平
    
    return need_double, double_choice

