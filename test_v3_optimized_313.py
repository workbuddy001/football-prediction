def analyze_match_v3_optimized(match):
    """V3优化版分析"""
    编号 = match["编号"]
    主胜 = match["主胜"]
    平局 = match["平局"]
    客胜 = match["客胜"]
    推荐 = match["推荐"]
    主队状态 = match["主队状态"]
    客队状态 = match["客队状态"]
    
    # 状态差距
    状态差距 = 主队状态 - 客队状态
    
    # 赔率区间判断
    if 主胜 < 1.35:
        赔率区间 = "极低"
    elif 主胜 < 1.5:
        赔率区间 = "低"
    elif 主胜 < 1.7:
        赔率区间 = "中低"
    elif 主胜 < 2.0:
        赔率区间 = "中"
    else:
        赔率区间 = "高"
    
    # 平局赔率判断
    if 平局 < 2.9:
        平局区间 = "极低"
    elif 平局 < 3.3:
        平局区间 = "低"
    elif 平局 < 3.7:
        平局区间 = "中"
    else:
        平局区间 = "高"
    
    # === V3优化版判断 ===
    
    # A级信号
    if 主胜 < 1.35 and 状态差距 > 20:
        # 极低赔 + 状态差距大 = 强队胜
        return "主胜", "A级", "极低赔+状态差距大=实盘"
    
    if 主胜 < 1.35 and 平局 < 3.0:
        # 极低赔 + 低平 = 防平
        if 客胜 > 6.0:
            return "主胜", "A级", "极低赔但客胜高=强队胜"
        else:
            return "防平", "B级", "极低赔+低平=防平"
    
    # B级信号：状态差距大 + 低赔
    if 主胜 < 1.6 and 状态差距 > 25:
        return "主胜", "B级", "状态差距大+低赔=实盘"
    
    # B级信号：澳门推荐+平赔适中
    if 推荐 == "胜" and 1.5 < 平局 < 3.5:
        return "主胜", "B级", "澳门推荐+平赔适中"
    
    # B级信号：防平（新增 - 基于欧赔核心思维的平局判断）
    if 平局 < 2.95:
        # 极低平局 = 重点防平
        return "防平", "B级", "极低平局赔率=重点防平"
    
    # B级信号：澳门推荐平局
    if 推荐 == "平":
        return "防平", "B级", "澳门推荐平局"
    
    # C级信号：状态相近
    if abs(状态差距) < 15:
        if 推荐 == "胜":
            return "主胜", "C级", "状态相近+澳门推荐胜"
        elif 推荐 == "负":
            return "客胜", "C级", "状态相近+澳门推荐负"
        else:
            return "防平", "C级", "状态相近+平局推荐"
    
    # 默认：跟随澳门推荐
    if 推荐 == "胜":
        return "主胜", "C级", "默认跟随澳门"
    elif 推荐 == "负":
        return "客胜", "C级", "默认跟随澳门"
    else:
        return "防平", "C级", "默认防平"

# 分析所有比赛
