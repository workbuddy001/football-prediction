# V6算法 - 使用完整30家公司数据
# 诱盘判断: 澳门推荐方向升10%以上公司>=50% = 诱盘
# 实盘判断: 澳门推荐方向降10%以上公司>=50% = 实盘

def get_distribution(home, draw, away):
    """判断分布类型"""
    if home < away and home < 2.0:
        if away - home > 0.5:
            return "顺分布"
        elif away - home > 0.2:
            return "缓冲分布"
        else:
            return "中庸分布"
    elif away < home and away < 2.0:
        if home - away > 0.5:
            return "逆分布"
        elif home - away > 0.2:
            return "缓冲分布"
        else:
            return "中庸分布"
    else:
        return "中庸分布"


def analyze_odds_change_30(initial_odds, realtime_odds):
    """
    精确统计30家公司赔率变化
    返回: 各选项升/降10%以上公司数
    """
    home_up_10 = 0
    home_down_10 = 0
    draw_up_10 = 0
    draw_down_10 = 0
    away_up_10 = 0
    away_down_10 = 0
    
    total = min(len(initial_odds), len(realtime_odds))
    
    for i in range(total):
        ih, id, ia = initial_odds[i]
        rh, rd, ra = realtime_odds[i]
        
        # 主胜变化
        if ih > 0:
            home_pct = (rh - ih) / ih * 100
            if home_pct >= 10:
                home_up_10 += 1
            elif home_pct <= -10:
                home_down_10 += 1
        
        # 平局变化
        if id > 0:
            draw_pct = (rd - id) / id * 100
            if draw_pct >= 10:
                draw_up_10 += 1
            elif draw_pct <= -10:
                draw_down_10 += 1
        
        # 客胜变化
        if ia > 0:
            away_pct = (ra - ia) / ia * 100
            if away_pct >= 10:
                away_up_10 += 1
            elif away_pct <= -10:
                away_down_10 += 1
    
    return {
        "home_up_10": home_up_10, "home_down_10": home_down_10,
        "draw_up_10": draw_up_10, "draw_down_10": draw_down_10,
        "away_up_10": away_up_10, "away_down_10": away_down_10,
        "total": total
    }


def analyze_macao_real_trap(macao_tip, home_team, away_team, home, draw, away, stats):
    """澳门心水判断实盘/诱盘"""
    # 支持繁体"贏"和简体"赢"
    macao_home = home_team in macao_tip and ("赢" in macao_tip or "贏" in macao_tip)
    macao_away = away_team in macao_tip and ("赢" in macao_tip or "贏" in macao_tip)
    macao_draw = "和局" in macao_tip
    
    total = stats["total"]
    
    if macao_home:
        # 主胜降赔多 = 庄家真看好主胜（实盘）
        # 客胜升赔多 = 庄家用客胜分散（诱盘指向主胜）
        if stats["home_down_10"] > stats["home_up_10"]:
            return "实盘", "主胜", f"主胜降赔{stats['home_down_10']}家→实盘"
        # 主胜升赔多 = 诱盘
        elif stats["home_up_10"] > stats["home_down_10"]:
            return "诱盘", "防平", f"主胜升赔{stats['home_up_10']}家→诱盘"
        return "实盘", "主胜", "跟澳门"
    
    elif macao_away:
        # 客胜降赔多 = 庄家真看好客胜（实盘）
        # 主胜升赔多 = 庄家用主胜分散（诱盘指向客胜）
        if stats["away_down_10"] > stats["away_up_10"]:
            return "实盘", "客胜", f"客胜降赔{stats['away_down_10']}家→实盘"
        # 客胜升赔多 = 诱盘
        elif stats["away_up_10"] > stats["away_down_10"]:
            return "诱盘", "防平", f"客胜升赔{stats['away_up_10']}家→诱盘"
        return "实盘", "客胜", "跟澳门"
    
    elif macao_draw:
        return "实盘", "和局", "澳门推荐和局"
    
    return None, None, "无澳门"


def v6_predict(code, home_team, away_team, macao_tip, home, draw, away, initial_odds, realtime_odds):
    """V6预测"""
    distribution = get_distribution(home, draw, away)
    stats = analyze_odds_change_30(initial_odds, realtime_odds)
    
    macao_type, result, reason = analyze_macao_real_trap(
        macao_tip, home_team, away_team, home, draw, away, stats
    )
    
    if macao_type:
        return result, "A级", distribution, f"{macao_type}-{reason}"
    
    # 无澳门时用分布
    if distribution == "顺分布":
        return "主胜" if home < 1.8 else "防平", "B级", distribution, "顺分布"
    elif distribution == "逆分布":
        return "客胜" if away < 1.8 else "防平", "B级", distribution, "逆分布"
    else:
        return "防平", "B级", distribution, "中庸"


