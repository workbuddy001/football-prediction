def analyze_match_v3_optimized(match):
    """V3优化版分析"""
    主胜 = match["主胜"]
    平局 = match["平局"]
    客胜 = match["客胜"]
    推荐 = match["推荐"]
    主队状态 = match["主队状态"]
    客队状态 = match["客队状态"]
    
    状态差距 = 主队状态 - 客队状态
    
    # A级信号
    if 主胜 < 1.35 and 状态差距 > 20:
        return "主胜", "A级", "极低赔+状态差距大=实盘"
    
    if 主胜 < 1.35 and 平局 < 3.0:
        if 客胜 > 6.0:
            return "主胜", "A级", "极低赔但客胜高=强队胜"
        else:
            return "防平", "B级", "极低赔+低平=防平"
    
    # B级信号
    if 主胜 < 1.6 and 状态差距 > 25:
        return "主胜", "B级", "状态差距大+低赔=实盘"
    
    if 推荐 == "胜" and 1.5 < 平局 < 3.5:
        return "主胜", "B级", "澳门推荐+平赔适中"
    
    if 平局 < 2.95:
        return "防平", "B级", "极低平局赔率=重点防平"
    
    if 推荐 == "平":
        return "防平", "B级", "澳门推荐平局"
    
    # C级信号
    if abs(状态差距) < 15:
        if 推荐 == "胜":
            return "主胜", "C级", "状态相近+澳门推荐胜"
        elif 推荐 == "负":
            return "客胜", "C级", "状态相近+澳门推荐负"
        else:
            return "防平", "C级", "状态相近+平局推荐"
    
    if 推荐 == "胜":
        return "主胜", "C级", "默认跟随澳门"
    elif 推荐 == "负":
        return "客胜", "C级", "默认跟随澳门"
    else:
        return "防平", "C级", "默认防平"

# 分析
results = []
for match in matches_314:
    编号 = match["编号"]
    预测, 把握度, 理由 = analyze_match_v3_optimized(match)
    实际 = actual_results_314.get(编号, "")
    
    正确 = (预测 == 实际) or (预测 == "防平" and "平" in 实际)
    
    results.append({
        "编号": 编号,
        "主队": match["主队"],
        "客队": match["客队"],
        "预测": 预测,
        "把握度": 把握度,
        "理由": 理由,
        "实际": 实际,
        "正确": 正确
    })

# 统计
total = len(results)
correct = sum(1 for r in results if r["正确"])
by_confidence = {}
for r in results:
    conf = r["把握度"]
    if conf not in by_confidence:
        by_confidence[conf] = {"total": 0, "correct": 0}
    by_confidence[conf]["total"] += 1
    if r["正确"]:
        by_confidence[conf]["correct"] += 1

