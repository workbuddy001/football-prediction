# 统计"推荐平局"的命中率

# 从输出中提取"推荐平局"的比赛
# 周五010 马赛 vs 欧塞尔: V7主胜(70%), 8变化-5, 实际主胜 -> 错
# 周六013 霍芬海姆 vs 沃夫斯堡: V7主胜(70%), 8变化-2, 实际平局 -> 对
# 周六016 勒沃库森 vs 拜仁: V7客胜(65%), 8变化-2, 实际平局 -> 对
# 周五007 坎布尔 vs 罗达JC: V7主胜(57%), 8变化-4, 实际平局 -> 对

print("=" * 60)
print("推荐平局 验证")
print("=" * 60)

matches = [
    ("周五010", "马赛vs欧塞尔", "主胜", "主胜", "错"),
    ("周六013", "霍芬海姆vs沃夫斯堡", "主胜", "平局", "对"),
    ("周六016", "勒沃库森vs拜仁", "客胜", "平局", "对"),
    ("周五007", "坎布尔vs罗达JC", "主胜", "平局", "对"),
]

correct = 0
for m in matches:
    mid, teams, pred, actual, result = m
    if result == "对":
        correct += 1
    print(f"{mid} {teams}: 预测={pred}, 实际={actual} [{result}]")

total = len(matches)
hit_rate = correct / total * 100

print(f"\n统计: {correct}/{total} = {hit_rate:.1f}%")
print("\n规律验证:")
print("- 周五010: 胜率差30%(主40%vs客10%)+8减少 → 但实际主胜(错)")
print("- 周六013: 胜率差30%(主30%vs客0%)+8减少 → 平局(对)")
print("- 周六016: 胜率差50%(客60%vs主10%)+8减少 → 平局(对)")
print("- 周五007: 胜率差20%(主50%vs客30%)+8减少 → 平局(对)")
print("\n结论: 4场中3场对，75%命中率！")
